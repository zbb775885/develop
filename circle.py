###################循环语句for#######################
names = ['bob', 'james', 'paul']
for name in names:
    print(name)
##################################################



#####################范围函数range################################
#range(100)可产生0-99这100个数据
sum = 0
tmp = range(100)
for i in tmp:
    sum += i
print("sum val:%d" %sum)
#list函数可将列表中的数据打印出来
print(list(tmp))
####################################################


###################循环语句while#######################
#用法
sum = 0
n = 99
while n > 0:
    sum += n
    n -= 1
print("sum val:%d" %sum)







